	function showServerStat() {
		$('.statistic_site').hide(1000);
		$('.statistic_server').show(1000);
		$('.change_stat_server').hide(1000);
		$('.change_stat_site').show(1000);
	}
	function showSiteStat() {
		$('.statistic_site').show(1000);
		$('.statistic_server').hide(1000);
		$('.change_stat_server').show(1000);
		$('.change_stat_site').hide(1000);
	}